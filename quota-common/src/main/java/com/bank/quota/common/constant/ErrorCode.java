package com.bank.quota.common.constant;

import lombok.AllArgsConstructor;
import lombok.Getter;

/**
 * 系统错误码枚举类
 * 
 * <p>定义银行级信贷额度管控平台所有业务场景的错误码和错误消息。
 * 错误码采用6位数字编码，按照业务模块进行分类。</p>
 * 
 * <h3>错误码编码规则：</h3>
 * <ul>
 *   <li>000000：成功状态码</li>
 *   <li>100XXX：系统级错误（参数异常、系统异常等）</li>
 *   <li>200XXX：额度相关错误</li>
 *   <li>300XXX：客户相关错误</li>
 *   <li>400XXX：集团相关错误</li>
 *   <li>500XXX：批复相关错误</li>
 *   <li>600XXX：合同相关错误</li>
 *   <li>700XXX：映射关系错误</li>
 *   <li>800XXX：白名单相关错误</li>
 *   <li>900XXX：锁和事务错误</li>
 *   <li>A00XXX：认证授权错误</li>
 * </ul>
 * 
 * <h3>使用示例：</h3>
 * <pre>{@code
 * // 抛出业务异常
 * throw new BusinessException(ErrorCode.QUOTA_INSUFFICIENT.getCode(), 
 *         ErrorCode.QUOTA_INSUFFICIENT.getMessage());
 * 
 * // 获取错误码信息
 * String code = ErrorCode.QUOTA_NOT_FOUND.getCode();    // "200001"
 * String message = ErrorCode.QUOTA_NOT_FOUND.getMessage(); // "额度不存在"
 * }</pre>
 * 
 * @author Quota System Team
 * @version 1.0.0
 * @since 2026-02-10
 * @see BusinessException
 */
@Getter
@AllArgsConstructor
public enum ErrorCode {
    
    // ==================== 成功状态 ====================
    
    /**
     * 操作成功
     */
    SUCCESS("000000", "成功"),
    
    // ==================== 系统级错误 (100XXX) ====================
    
    /**
     * 系统异常
     * <p>用于未预期的系统级异常，如数据库连接失败、网络超时等</p>
     */
    SYSTEM_ERROR("100001", "系统异常"),
    
    /**
     * 参数异常
     * <p>用于请求参数校验失败、参数格式错误等场景</p>
     */
    PARAM_ERROR("100002", "参数异常"),
    
    /**
     * 数据不存在
     * <p>用于查询数据时未找到对应记录的场景</p>
     */
    DATA_NOT_FOUND("100003", "数据不存在"),
    
    /**
     * 数据已存在
     * <p>用于创建数据时发现重复记录的场景</p>
     */
    DATA_ALREADY_EXISTS("100004", "数据已存在"),
    
    // ==================== 额度相关错误 (200XXX) ====================
    
    /**
     * 额度不存在
     * <p>查询额度记录时未找到对应数据</p>
     */
    QUOTA_NOT_FOUND("200001", "额度不存在"),
    
    /**
     * 额度不足
     * <p>额度占用、锁定时可用额度不足</p>
     */
    QUOTA_INSUFFICIENT("200002", "额度不足"),
    
    /**
     * 额度已冻结
     * <p>对已冻结的额度进行操作时抛出</p>
     */
    QUOTA_FROZEN("200003", "额度已冻结"),
    
    /**
     * 额度已过期
     * <p>操作已过期的额度时抛出</p>
     */
    QUOTA_EXPIRED("200004", "额度已过期"),
    
    /**
     * 额度多重占用错误
     * <p>额度多重占用时抛出</p>
     */
    QUOTA_MULTI_OCCUPY_ERROR("200005", "额度多重占用错误"),
    
    // ==================== 客户相关错误 (300XXX) ====================
    
    /**
     * 客户不存在
     * <p>查询客户信息时未找到对应记录</p>
     */
    CUSTOMER_NOT_FOUND("300001", "客户不存在"),
    
    /**
     * 客户类型错误
     * <p>客户类型不符合业务要求时抛出</p>
     */
    CUSTOMER_TYPE_ERROR("300002", "客户类型错误"),
    
    // ==================== 集团相关错误 (400XXX) ====================
    
    /**
     * 集团不存在
     * <p>查询集团信息时未找到对应记录</p>
     */
    GROUP_NOT_FOUND("400001", "集团不存在"),
    
    // ==================== 批复相关错误 (500XXX) ====================
    
    /**
     * 批复不存在
     * <p>查询批复信息时未找到对应记录</p>
     */
    APPROVAL_NOT_FOUND("500001", "批复不存在"),
    
    /**
     * 批复已过期
     * <p>操作已过期的批复时抛出</p>
     */
    APPROVAL_EXPIRED("500002", "批复已过期"),
    
    // ==================== 合同相关错误 (600XXX) ====================
    
    /**
     * 合同不存在
     * <p>查询合同信息时未找到对应记录</p>
     */
    CONTRACT_NOT_FOUND("600001", "合同不存在"),
    
    /**
     * 合同占用异常
     * <p>合同额度占用过程中发生异常</p>
     */
    CONTRACT_OCCUPY_ERROR("600002", "合同占用异常"),
    
    // ==================== 映射关系错误 (700XXX) ====================
    
    /**
     * 映射关系不存在
     * <p>查询业务品种与授信产品映射时未找到记录</p>
     */
    MAPPING_NOT_FOUND("700001", "映射关系不存在"),
    
    /**
     * 映射关系错误
     * <p>映射关系配置错误或不符合业务规则</p>
     */
    MAPPING_ERROR("700002", "映射关系错误"),
    
    // ==================== 白名单相关错误 (800XXX) ====================
    
    /**
     * 白名单不存在
     * <p>查询白名单时未找到对应记录</p>
     */
    WHITELIST_NOT_FOUND("800001", "白名单不存在"),
    
    /**
     * 白名单已过期
     * <p>操作已过期的白名单记录时抛出</p>
     */
    WHITELIST_EXPIRED("800002", "白名单已过期"),
    
    /**
     * 白名单无效
     * <p>白名单状态无效或已被禁用</p>
     */
    WHITELIST_INVALID("800003", "白名单无效"),
    
    // ==================== 锁和事务错误 (900XXX) ====================
    
    /**
     * 获取锁失败
     * <p>分布式锁获取失败，可能存在并发冲突</p>
     */
    LOCK_ERROR("900001", "获取锁失败"),
    
    /**
     * 事务执行失败
     * <p>分布式事务执行过程中发生异常</p>
     */
    TRANSACTION_ERROR("900002", "事务执行失败"),
    
    /**
     * 获取分布式锁失败
     * <p>TCC事务获取分布式锁超时或失败</p>
     */
    LOCK_ACQUIRE_FAILED("900003", "获取分布式锁失败"),
    
    /**
     * 事务不存在
     * <p>TCC事务日志未找到对应记录</p>
     */
    TRANSACTION_NOT_FOUND("900004", "事务不存在"),
    
    /**
     * 额度占用失败
     * <p>TCC事务Try阶段额度占用失败</p>
     */
    QUOTA_OCCUPY_FAILED("900005", "额度占用失败"),
    
    // ==================== 认证授权错误 (A00XXX) ====================
    
    /**
     * 认证失败
     * <p>用户身份认证失败，如token无效、密码错误等</p>
     */
    AUTH_ERROR("A00001", "认证失败"),
    
    /**
     * 权限不足
     * <p>用户无权限执行当前操作</p>
     */
    PERMISSION_DENIED("A00002", "权限不足"),

    /**
     * 操作不允许
     * <p>当前状态下不允许执行该操作</p>
     */
    OPERATION_NOT_ALLOWED("A00003", "操作不允许"),

    /**
     * 访问被拒绝
     * <p>访问权限被拒绝</p>
     */
    ACCESS_DENIED("A00004", "访问被拒绝"),

    /**
     * 数据操作不允许
     * <p>数据操作不符合业务规则</p>
     */
    DATA_NOT_ALLOWED("A00005", "数据操作不允许");

    /**
     * 错误码
     */
    private final String code;
    
    /**
     * 错误消息
     */
    private final String message;
}
